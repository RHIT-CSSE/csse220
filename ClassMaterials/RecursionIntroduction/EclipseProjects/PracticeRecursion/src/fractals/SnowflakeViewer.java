package fractals;

import javax.swing.JFrame;

public class SnowflakeViewer {

	private JFrame myFrame;
	
	public SnowflakeViewer() {
		
		this.myFrame = new JFrame("Snowflake App");
		this.myFrame.setSize(800,600);
		this.myFrame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
		this.myFrame.add(new SnowflakeComponent() );
		
		this.myFrame.setVisible(true);
		
		
	}
	
	public static void main(String[] args) {
		SnowflakeViewer app = new SnowflakeViewer();
	}
	
}
